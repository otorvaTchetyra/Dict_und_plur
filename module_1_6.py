# Создание словаря
my_dict = {"Иван": 1990, "Петр": 1995, "Анна": 1997}

# Вывод словаря на экран
print(my_dict)

# Вывод значения по существующему ключу
print(my_dict["Иван"])

# Вывод значения по отсутствующему ключу без ошибки
try:
    print(my_dict["Екатерина"])
except KeyError:
    print("Ключ 'Екатерина' не найден в словаре.")

# Добавление пар в словарь
my_dict["Мария"] = 1999
my_dict["Алексей"] = 1992

# Удаление пары по ключу
del my_dict["Анна"]

# Вывод удаленной пары
print(my_dict.pop("Петр"))

# Вывод измененного словаря
print(my_dict)

# Создание множества
my_set = {1, "привет", 1, "мир", 2, 3, "мир"}

# Вывод множества на экран
print(my_set)

# Добавление элементов в множество
my_set.add(4)
my_set.add("здравствуйте")

# Удаление элемента из множества
my_set.discard(1)

# Вывод измененного множества
print(my_set)